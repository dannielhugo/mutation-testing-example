export function add(n1: number, n2: number): number {
  return n1 + n2;
}

export function sub(n1: number, n2: number): number {
  return n1 - n2;
}

export function multiply(n1: number, n2: number): number {
  return n1 * n2;
}

export function divide(n1: number, n2: number): number {
  return n1 / n2;
}
